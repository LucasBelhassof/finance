    SELECT 1
    FROM pg_constraint
    WHERE conname = 'bank_connections_invoice_due_reminder_days_check'
  ) THEN
    ALTER TABLE bank_connections
    ADD CONSTRAINT bank_connections_invoice_due_reminder_days_check
