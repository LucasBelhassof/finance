CREATE TABLE IF NOT EXISTS invoice_notification_events (
  id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  user_id INTEGER NOT NULL REFERENCES users(id) ON DELETE CASCADE,
  bank_connection_id INTEGER NOT NULL REFERENCES bank_connections(id) ON DELETE CASCADE,
  invoice_period_end DATE NOT NULL,
  event_type TEXT NOT NULL CHECK (event_type IN ('invoice_closed', 'invoice_due_soon')),
  notification_id INTEGER REFERENCES notifications(id) ON DELETE CASCADE,
  created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
  UNIQUE (user_id, bank_connection_id, invoice_period_end, event_type)
);

CREATE INDEX IF NOT EXISTS idx_invoice_notification_events_user_card
ON invoice_notification_events (user_id, bank_connection_id, invoice_period_end DESC);
